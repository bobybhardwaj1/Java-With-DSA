package corejava;

public class logicalop {
    public static void main(String[] args) {
        int p=10,q=20,r=30;
        // AND(&&) operator
        System.out.println((p>q)&&(q>r));
        // OR(||) operator
        System.out.println((p>q)||(q>r));
        // NOT(!) operator
        System.out.println(!(p>q));
    }
    
}
