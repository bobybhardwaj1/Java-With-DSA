package corejava;

public class assignmentop {
    public static void main(String[] args) {
        int p =20;
        int q;
        // = operator
        q=p;
        System.out.println(q);
        // += operator
        p+=q;
        System.out.println(p);
        // -= operator
        p-=q;
        System.out.println(p);
        // *= operator
        p*=q;
        System.out.println(p);
        // /= oprator
        p/=q;
        System.out.println(p);
        // %= operator
        p%=q;
        System.out.println(p);

    }
    
}
