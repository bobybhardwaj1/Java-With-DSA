package corejava;

public class unaryop {
    public static void main(String[] args) {
        int p=5,q=5;
        System.out.println(p++);
        System.out.println(p);
        System.out.println(q++);
        System.out.println(q);
        System.out.println(++p);
        System.out.println(++q);
        int x=++q;
        System.out.println(x);
        int y=q++;
        System.out.println(y);

    }
    
}
