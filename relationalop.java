package corejava;

public class relationalop {
    public static void main(String[] args) {
        int p=2,q=3;
        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>q);
        System.out.println(p<q);
        System.out.println(p>=q);
        System.out.println(p<=q);
    }
    
}
